#include <iostream>
using namespace std;

int main(void) {
	// 转义字符

	// 换行符 \n
	cout << "hello C++\n";
	
	// 反斜杠 '\\'
	cout << "\\" << endl;

	// 水平制表符 \t	作用：整齐的输出数据
	cout << "aaa\thello C++" << endl;
	cout << "aa\thello C++" << endl;
	cout << "aaaaaa\thello C++" << endl;
	
	// system("pause");
	return 0;
}