#include <iostream>
using namespace std;

int main(void) {
	// 比较运算符

	// == 
	int a = 10, b = 20;
	cout << (a == b) << endl;	// 注意优先级

	// !=
	cout << (a != b) << endl;

	// >
	cout << (a > b) << endl;

	// <
	cout << (a < b) << endl;

	// >=
	cout << (a >= b) << endl;

	// <=
	cout << (a <= b) << endl;

	// system("pause");
	return 0;
}